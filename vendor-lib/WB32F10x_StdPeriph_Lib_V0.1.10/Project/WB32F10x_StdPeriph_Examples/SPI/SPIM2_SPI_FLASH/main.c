int main(void)
{
  /* Init UART1 to 115200-8n1 for print message */
  uart1_init(96000000, 115200);

  /* Initialize the SPI FLASH driver */
  SpiFlash_Init();

  printf("SPI example with SPI Flash\r\n");

  /* Get SPI Flash ID */
  jedec_id = SpiFlash_ReadIdentification();
  printf("This flash chip JEDEC ID is 0x%06X\r\n", jedec_id);

  /* Perform a write in the Flash followed by a read of the written data */
  /* Erase SPI FLASH Sector to write on */
  SpiFlash_SectorErase(FLASH_SECTOR_TO_ERASE);

  /* Write WriteData data to SPI FLASH memory */
  SpiFlash_WriteBuffer(FLASH_WRITE_ADDRESS, WriteData, sizeof(WriteData));

  /* Read data from SPI FLASH memory */
  SpiFlash_ReadData(FLASH_READ_ADDRESS, ReadBuffer, sizeof(ReadBuffer));

  /* Check the correctness of written data */
  if(memcmp(WriteData, ReadBuffer, sizeof(WriteData)) == 0)
  {
    printf("Flash progam and read operation ok.\r\n");
  }
  else
  {
    printf("Flash progam and read operation failed!\r\n");
  }

  /* Infinite loop */
  while (1)
  {
  }
}

