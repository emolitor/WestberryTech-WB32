int main(void)
{
  uint32_t iter;

  /* Enable GPIOAï¼Œ GPIOB and UART1 clock */
  RCC_APB1PeriphClockCmd(RCC_APB1Periph_BMX1 |RCC_APB1Periph_GPIOA |RCC_APB1Periph_GPIOB |RCC_APB1Periph_UART1, ENABLE);

  /*
    PB8  (UART1_CTS)
    PB9  (UART1_RTS)
    PA9  (UART1_TX)
    PA10 (UART1_RX)
  */
  GPIO_Init(GPIOB, GPIO_Pin_8 |GPIO_Pin_9, GPIO_MODE_AF |GPIO_OTYPE_PP |GPIO_PUPD_UP |GPIO_SPEED_HIGH |GPIO_AF7);
  GPIO_Init(GPIOA, GPIO_Pin_9 |GPIO_Pin_10, GPIO_MODE_AF |GPIO_OTYPE_PP |GPIO_PUPD_UP |GPIO_SPEED_HIGH |GPIO_AF7);

  /* UART1 configuration */
  UART_DeInit(UART1);
  UART_InitStructure.UART_BaudRate = 9600;
  UART_InitStructure.UART_WordLength = UART_WordLength_8b;
  UART_InitStructure.UART_StopBits = UART_StopBits_One;
  UART_InitStructure.UART_Parity = UART_Parity_None;
  UART_InitStructure.UART_AutoFlowControl = UART_AutoFlowControl_RTS_CTS;
  UART_Init(UART1, &UART_InitStructure);
  UART_RxFIFOThresholdConfig(UART1, UART_RxFIFOThreshold_8);
  UART_FIFOCmd(UART1, ENABLE);

  /* Send data */
  for(iter = 0; iter < 300; iter++)
  {
    while(UART_GetFlagStatus(UART1, UART_FLAG_TFNF) == RESET);
    UART_WriteData(UART1, testdata[iter]);
  }

  /* Receive data */
  for(iter = 0; iter < 300; iter++)
  {
    while(UART_GetFlagStatus(UART1, UART_FLAG_RFNE) == RESET);
    rxbuffer[iter] = UART_ReadData(UART1);
  }

  /* Infinite loop */
  while (1)
  {
  }
}

